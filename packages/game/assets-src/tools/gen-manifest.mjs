function entry(
  path,
  srcKey,
  { derivedFrom, processing, placeholder, author, sourceDownload, sourceSha256 } = {},
) {
  const s = SOURCES[srcKey];
  return {
    path,
    author: author ?? s.author,
    source: s.url,
    sourceDownload: sourceDownload ?? s.download,
    sourceSha256: sourceSha256 ?? s.sha256,
    license: s.license,
    licenseVerifiedAt: VERIFIED,
    derivedFrom,
    processing,
    ...(placeholder ? { placeholder: true } : {}),
  };
}

/** Dual-format M3 audio entry pair (.ogg + .m4a — GDD §11.5 双格式). */
function audioPair(basePath, srcKey, opts) {
  return [entry(`${basePath}.ogg`, srcKey, opts), entry(`${basePath}.m4a`, srcKey, opts)];
}

