const WHITELIST = new Set(['CC0-1.0', 'OFL-1.1']);

const manifest = {
  generatedBy: 'assets-src/tools/gen-manifest.mjs',
  licenseWhitelist: [...WHITELIST],
  files: ENTRIES.map((e) => {
    if (!WHITELIST.has(e.license))
      throw new Error(`license not whitelisted: ${e.license} (${e.path})`);
    const buf = readFileSync(join(gameRoot, e.path));
    return { ...e, sha256: createHash('sha256').update(buf).digest('hex'), bytes: buf.length };
  }),
};

writeFileSync(join(gameRoot, 'assets/manifest.json'), JSON.stringify(manifest, null, 2) + '\n');
console.log(
  `manifest.json: ${manifest.files.length} files, total ${manifest.files.reduce((a, f) => a + f.bytes, 0)} bytes`,
);
