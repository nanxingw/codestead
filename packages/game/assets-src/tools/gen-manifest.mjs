function entry(path, srcKey, { derivedFrom, processing, placeholder } = {}) {
  const s = SOURCES[srcKey];
  return {
    path,
    author: s.author,
    source: s.url,
    sourceDownload: s.download,
    sourceSha256: s.sha256,
    license: s.license,
    licenseVerifiedAt: VERIFIED,
    derivedFrom,
    processing,
    ...(placeholder ? { placeholder: true } : {}),
  };
}

